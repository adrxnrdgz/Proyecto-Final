    private void btnAgtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgtActionPerformed
        
        Connect();
        try {
        //Obtemnemos los datos de los campos de texto 
        String cod = txtCod.getText();
        String pnombre = txtPnombre.getText();
        String cantidad = txtCant.getText();
        

        //Hacemos una consulta sql para verificar si hay producto existente
        pst = con.prepareStatement("select cantidad from product where cod = ? and pnombre = ?");
        pst.setString(1, cod);
        pst.setString(2, pnombre);
        ResultSet rs = pst.executeQuery();
        int cantidadInv = 0;

        if (rs.next()) {
            cantidadInv = rs.getInt("cantidad");
        
        //Se calcula la nueva cantidad despues de la transaccion
        int nuevaExist = cantidadInv + Integer.parseInt(cantidad);

        //Actualizamos la base de datos con la nueva cantidad
        pst = con.prepareStatement("update product set cantidad = ? where cod = ? and pnombre = ?");
        pst.setInt(1, nuevaExist);
        pst.setString(2, cod);
        pst.setString(3, pnombre);
        pst.executeUpdate();
        
        //Se muestra un mensaje de exito en la transaccion
        JOptionPane.showMessageDialog(this, "Transacción Completada");

        //Limpiamos los campos de texto
        txtPnombre.setText("");
        txtCant.setText("");
        txtCod.setText("");
        
        //Llamamos al metodo load para realizar su funcion
        load(cod);
        
        
        } else{
            JOptionPane.showMessageDialog(this, "Producto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
        txtPnombre.setText("");
        txtCant.setText("");
        txtCod.setText("");
           
    } catch (SQLException ex) {
        Logger.getLogger(Transacciones.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_btnAgtActionPerformed

