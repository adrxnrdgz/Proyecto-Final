    public void load(String cod){
        
        try {
        Connect();
        //Se prepara la consulta a sql para obtener todas las transacciones
        pst = con.prepareStatement("select id, cod, pnombre, cantidad from product where cod = ?");
        pst.setString(1, cod);

        ResultSet rs = pst.executeQuery();

        //Se obtienen los metadatos de los conjuntos de resultados
        ResultSetMetaData rd = rs.getMetaData();
        int columnCount = rd.getColumnCount();
        
        //Obtenemos el modelo de la tabla y se borran todas las filas existentes
        df = (DefaultTableModel)tblTrans.getModel();
        df.setRowCount(0);

        //Itera sobre el conjunto de resultados y agrega cada fila a la tabla
        while (rs.next()) {
            Vector rowData = new Vector();
            for (int i = 1; i <= columnCount; i++) {
                rowData.add(rs.getString(i));
            }
            df.addRow(rowData);
        }
        
        //El catch se utiliza para manejar las excepciones relacionadas con la base de datos 
    } catch (SQLException ex) {
        Logger.getLogger(Transacciones.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
            //Cerramos la conexion despues de haberla utilizado
        try {
            if (con != null) {
                con.close();  
            }
        } catch (SQLException ex) {
            Logger.getLogger(Transacciones.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
}
    
