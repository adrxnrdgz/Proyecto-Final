    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        
        try {
            String pnombre = txtPnombre.getText();
            String descripcion = txtDesc.getText();
            String cod = txtCod.getText();
            String preciocosto = txtCost.getText();
            String retail = txtRetail.getText();
            String cantidad = txtCant.getText();
            String reordenar = txtReordenar.getText();
            
            
            pst = con.prepareStatement("insert into product(pnombre, descripcion, cod, preciocosto, retail, cantidad, reordenar) VALUES(?,?,?,?,?,?,?)");
            pst.setString(1, pnombre);
            pst.setString(2, descripcion);
            pst.setString(3, cod);
            pst.setString(4, preciocosto);
            pst.setString(5, retail);
            pst.setString(6, cantidad);
            pst.setString(7, reordenar);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Producto Agregado");
            
            txtPnombre.setText("");
            txtDesc.setText("");
            txtCod.setText("");
            txtCost.setText("");
            txtRetail.setText("");
            txtCant.setText("");
            txtReordenar.setText("");
            txtPnombre.requestFocus();
            load();
            
        } catch (SQLException ex) {
            Logger.getLogger(Producto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

