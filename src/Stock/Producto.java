     public void load(){
        
        int a;
        try {
            pst = con.prepareStatement("select * from product");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)tblProducto.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for (int i = 1; i <= a; i++) {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("pnombre"));
                    v2.add(rs.getString("descripcion"));
                    v2.add(rs.getString("cod"));
                    v2.add(rs.getString("preciocosto"));
                    v2.add(rs.getString("retail"));
                    v2.add(rs.getString("cantidad"));
                    v2.add(rs.getString("reordenar"));
                    
                }
                df.addRow(v2);
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Producto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
   
