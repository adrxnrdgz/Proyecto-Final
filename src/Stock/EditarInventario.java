    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        
        try {
            String pnombre = txtNombrep.getText();
            String descripcion = txtDesc.getText();
            String cod = txtCod.getText();
            String cantidad = txtCant.getText();
            
            pst = con.prepareStatement("update product set pnombre = ?, descripcion = ?, cantidad = ? where cod = ?");
            pst.setString(1, pnombre);
            pst.setString(2, descripcion);
            pst.setString(3, cantidad);
            pst.setString(4, cod);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Cambios Guardados");
            
        } catch (SQLException ex) {
            Logger.getLogger(EditarInventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

