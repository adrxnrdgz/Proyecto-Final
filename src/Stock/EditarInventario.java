    public void codigoProducto(){
        
        try {
            String codp = txtCod.getText();
            
            pst = con.prepareStatement("select * from product where cod = ?");
            pst.setString(1, codp);
            rs = pst.executeQuery();
            
            if(rs.next() == false)
            {
                JOptionPane.showMessageDialog(this, "Código no encontrado");
                txtCod.setText("");
            
            }
            else{
                
                String nombrep = rs.getString("pnombre");
                String desc = rs.getString("descripcion");
                String cant = rs.getString("cantidad");
                
                txtNombrep.setText(nombrep.trim());
                txtDesc.setText(desc.trim());
                txtCant.setText(cant.trim());
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EditarInventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

